<?php

namespace Troob\ApiBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * StoredMedia
 *
 * @ORM\Table()
 * @ORM\Entity
 */
class StoredMedia
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;
    
    /**
     * @var integer
     *
     * @ORM\Column(name="media_id", type="integer")
     */
    private $media_id;

    /**
     * @var string
     *
     * @ORM\Column(name="path", type="string", length=255)
     */
    private $path;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;

    /**
     * @var string
     *
     * @ORM\Column(name="type", type="string", length=255)
     */
    private $type;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set path
     *
     * @param string $path
     * @return StoredMedia
     */
    public function setPath($path)
    {
        $this->path = $path;
    
        return $this;
    }

    /**
     * Get path
     *
     * @return string 
     */
    public function getPath()
    {
        return $this->path;
    }

    /**
     * Set media id
     *
     * @param integer $path
     * @return StoredMedia
     */
    public function setMediaId($media_id)
    {
    	$this->media_id = $media_id;
    
    	return $this;
    }
    
    /**
     * Get path
     *
     * @return string
     */
    public function getMediaId()
    {
    	return $this->media_id;
    }
    
    /**
     * Set name
     *
     * @param string $name
     * @return StoredMedia
     */
    public function setName($name)
    {
        $this->name = $name;
    
        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set type
     *
     * @param string $type
     * @return StoredMedia
     */
    public function setType($type)
    {
        $this->type = $type;
    
        return $this;
    }

    /**
     * Get type
     *
     * @return string 
     */
    public function getType()
    {
        return $this->type;
    }
}
